public class SecretKey {

    public String client;
    public String secret;
    public String account;
    public Boolean active;

    public SecretKey() {

    }
    public String getClient() {
        return client;
    }
    public String getSecret() {
        return secret;
    }
    public String getAccount() {
        return account;
    }
    public Boolean getActive() {
        return active;
    }

}
